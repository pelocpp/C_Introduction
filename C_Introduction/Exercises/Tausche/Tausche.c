#include <stdio.h>

static void swap(int* px, int* py)
{
    int tmp = *py;

    *py = *px;

    *px = tmp;
}

void exercise_tausche()
{
    printf("Funktion swap\n");
    printf("=============\n");

    int n = 123;
    int m = 456;

    printf("n = % d, m = % d\n", n, m);

    swap(&n, &m);

    printf("n = % d, m = % d\n", n, m);
}

